    public function authenticate(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required|min:6'
        ]);

        $credentials = $request->only('email', 'password');

        if (Auth::attempt($credentials)) {
            return redirect('/')->withSuccess('Logined successfully');
        } else {
            return redirect('login')->withError('Login details are incorrect');
        }
    }

    public function register(Request $request)
    {
        $request->validate([
            'name' => 'required|alpha_dash:ascii',
            'email' => 'required|email|unique:users',
            'password' => 'required|min:6',
            'passwordConfirm' => 'required|min:6|same:password'
        ]);

        $data = $request->all();
        $check = $this->create($data);

        return redirect('/')->withSuccess('Account created successfully');
    }

